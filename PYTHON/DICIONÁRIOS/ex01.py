# Crie um dicionário chamado 'pessoa' com as seguintes chaves e valores:

pessoa = {
    "nome": "Ana",
    "idade": 25,
    "cidade": "São Paulo"
}

# acessando os valores associados as chaves
print(f"Este é o nome: {pessoa['nome']}")
print(f"Esta é a idade {pessoa['idade']}")
print(f"Esta é a cidade dela: {pessoa['cidade']}")
